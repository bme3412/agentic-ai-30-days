function initCompose() {
    const palette = document.querySelectorAll('.palette-item');
    const slots = document.querySelectorAll('.client-slot');

    palette.forEach(item => {
        item.addEventListener('dragstart', (e) => {
            e.dataTransfer.setData('server', item.dataset.server);
        });
    });

    slots.forEach(slot => {
        slot.addEventListener('dragover', (e) => {
            e.preventDefault();
            slot.classList.add('dragover');
        });

        slot.addEventListener('dragleave', () => {
            slot.classList.remove('dragover');
        });

        slot.addEventListener('drop', (e) => {
            e.preventDefault();
            slot.classList.remove('dragover');

            const server = e.dataTransfer.getData('server');
            if (server && slot.classList.contains('empty')) {
                fillSlot(slot, server);
            }
        });
    });

    document.getElementById('add-slot').addEventListener('click', addSlot);
    document.getElementById('clear-canvas').addEventListener('click', clearCanvas);
    document.getElementById('copy-config').addEventListener('click', () => {
        const code = document.getElementById('compose-output').textContent;
        copyToClipboard(code);
    });

    updateComposeConfig();
}

function fillSlot(slot, serverType) {
    const config = serverConfigs[serverType];

    slot.classList.remove('empty');
    slot.classList.add('filled');
    slot.innerHTML = `
        <div class="slot-content">
            <div class="slot-icon">${config.icon}</div>
            <div class="slot-name">${config.name}</div>
        </div>
        <button class="slot-remove" onclick="clearSlot(this.parentElement)">×</button>
    `;
    slot.dataset.server = serverType;

    updateComposeConfig();
}

function clearSlot(slot) {
    slot.classList.remove('filled');
    slot.classList.add('empty');
    slot.innerHTML = '<span class="slot-label">Drop server here</span>';
    delete slot.dataset.server;
    updateComposeConfig();
}

function addSlot() {
    const container = document.getElementById('canvas-clients');
    const slotCount = container.querySelectorAll('.client-slot').length;

    if (slotCount >= 6) {
        showToast('Maximum 6 servers');
        return;
    }

    const slot = document.createElement('div');
    slot.className = 'client-slot empty';
    slot.dataset.slot = slotCount + 1;
    slot.innerHTML = '<span class="slot-label">Drop server here</span>';

    slot.addEventListener('dragover', (e) => {
        e.preventDefault();
        slot.classList.add('dragover');
    });

    slot.addEventListener('dragleave', () => {
        slot.classList.remove('dragover');
    });

    slot.addEventListener('drop', (e) => {
        e.preventDefault();
        slot.classList.remove('dragover');

        const server = e.dataTransfer.getData('server');
        if (server && slot.classList.contains('empty')) {
            fillSlot(slot, server);
        }
    });

    container.appendChild(slot);
}

function clearCanvas() {
    document.querySelectorAll('.client-slot').forEach((slot, index) => {
        if (index < 3) {
            clearSlot(slot);
        } else {
            slot.remove();
        }
    });
    updateComposeConfig();
}

function updateComposeConfig() {
    const slots = document.querySelectorAll('.client-slot.filled');
    const config = { mcpServers: {} };

    slots.forEach(slot => {
        const serverType = slot.dataset.server;
        const serverConfig = serverConfigs[serverType];

        config.mcpServers[serverType] = {
            command: serverConfig.command,
            args: serverConfig.args,
            env: {}
        };
    });

    const codeEl = document.getElementById('compose-output');
    codeEl.textContent = JSON.stringify(config, null, 2);
    Prism.highlightElement(codeEl);
}

