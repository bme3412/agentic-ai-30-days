function analyzeSecurity() {
    const code = document.getElementById('security-code').value;
    const results = {};
    let passed = 0;
    let total = 0;

    for (const [check, config] of Object.entries(securityPatterns)) {
        if (config.optional && !code.match(config.patterns?.[0])) {
            results[check] = 'pending';
            continue;
        }

        total++;
        let pass = true;

        // Check for antipatterns (bad practices)
        if (config.antipatterns) {
            for (const pattern of config.antipatterns) {
                if (pattern.test(code)) {
                    pass = false;
                    break;
                }
            }
        }

        // Check for required patterns
        if (pass && config.patterns) {
            const required = config.required || 1;
            let found = 0;
            for (const pattern of config.patterns) {
                if (pattern.test(code)) found++;
            }
            if (found < required) pass = false;
        }

        results[check] = pass ? 'pass' : 'fail';
        if (pass) passed++;
    }

    // Update UI
    const score = total > 0 ? Math.round((passed / total) * 100) : 0;
    const scoreEl = document.getElementById('score-value');
    const circleEl = document.getElementById('score-circle');

    scoreEl.textContent = score;
    circleEl.style.setProperty('--score', score);
    circleEl.className = 'score-circle ' + (score >= 80 ? 'good' : score >= 50 ? 'warning' : 'danger');

    // Update checklist
    document.querySelectorAll('.check-item').forEach(item => {
        const check = item.dataset.check;
        const result = results[check];
        item.className = 'check-item ' + result;
    });
}

